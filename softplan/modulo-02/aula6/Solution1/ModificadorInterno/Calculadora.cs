using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ModificadorInterno
{
    internal class Calculadora
    {
        internal int CalcularSoma(int valorA, int valorB)
        {
            return valorA + valorB;
        }
        internal int CalcularSubtracao(int valorA, int valorB)
        {
            return valorA - valorB;
        }
    }
}
